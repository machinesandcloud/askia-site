function groupBy(keyFn) {
  return items.reduce((acc, item) => {
    const keys = keyFn(item);
    for (const key of keys) {
      if (!acc[key]) acc[key] = [];
      acc[key].push(item);
    }
    return acc;
  }, {});
}

